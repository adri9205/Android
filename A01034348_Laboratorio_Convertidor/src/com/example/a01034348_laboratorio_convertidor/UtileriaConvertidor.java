package com.example.a01034348_laboratorio_convertidor;

public class UtileriaConvertidor {
public static double convertirKM_millas(double km){
	
	return km*0.62;
}

public static double convertirKM_pulgadas(double km){
	
	return km*39370.1;
}

public static double convertirKM_metros(double km){
	
	return km*1000;
}
}
